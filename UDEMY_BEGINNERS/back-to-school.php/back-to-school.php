<?php

// Area of rectangle

$length = 10;

$width = 5;

$areaRectangle = $length * $width;

echo "The area of a rectangle with length = $length and width = $width is $areaRectangle";

// Area of triangle

$base = 12;

$height = 3;

$areaTriangle = $base * $height / 2;

echo "<br>" . $areaTriangle;

// Area of a circle

$radius = 5;

$pi = 3.14;

$areaCircle = $pi * pow($radius, 2);

echo "<br>" . $areaCircle;

$areaCircle = pi() * pow($radius, 2);

echo "<br>" . $areaCircle;

// Area of a trapezium

$a = 10;

$b = 8;

$height = 4;

$area = ($a + $b) * $height / 2;

echo "<br>" . $area;


















